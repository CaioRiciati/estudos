public class A22Revisão {
    public static void main(String[] args) {
        int[] numeros = {2,6,1,4,3,6,3,4,6,8};
        int soma = 0;


            
        
        for (int i = 0; i < numeros.length; i++) { 
            if (numeros [i] % 2 == 0) {
            soma += numeros[0];
            }
        }
    
    


        System.out.println("A soma dos números pares é: " + soma);
        
 
        System.out.println(soma);

    
    }
}
